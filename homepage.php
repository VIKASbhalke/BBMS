<p style="background-color: LightSalmon;">
	Donating blood is a simple thing to do, but it can make a big difference in the lives of others. The donation process from the time you arrive until the time you leave takes about 30 minutes. <br>
	</p>
<h2>Before Donating Blood</h2>
<p style="background-color: LightSalmon;">
	• Eat something substantial before your appointment.<br>
	• Have plenty of liquid the day before donation, especially in warm weather. In the 3 hours before donating, have at least 3 to 4 good sized glasses of water/juice.<br>
	</p>
<h3>Registration of Donor</h3>
<p style="background-color: LightSalmon;">
	Our volunteerstaff will ask you to fill a form with brief information on contact details and medical history.<br>
	</p>
<h4>Physical Health check-up</h4>
<p style="background-color: LightSalmon;">
	You will be examined by a doctor to answer some questions about your health history Your blood pressure and hemoglobin level will be checked.<br>
	</p>
	<center>
	<img src="pic4.jpg"  width="1000px" height="928px"/>
	</center>
<h5>Blood Donation</h5>
<p style="background-color: LightSalmon;">
	We will cleanse an area on your arm and insert a brand new sterile needle for the blood draw This feels like a quick pinch and is over in seconds.<br>
	• The actual donation takes about 8-10 minutes, during which will be seated comfortably.<br>
	• When a unit of blood (350-450 ml) has been collected, the donation is complete.<br>
	• A staff person will place a bandage on your arm.<br>
	</p>
	<img src="pic3.jpg"  width="500px" height="131px"/>
<h6>Safety</h6>
<p style="background-color: LightSalmon;">
	All equipment is sterile, needles are used only once and then discarded. In the great majority of individuals, a donation of 450 ml a less than 10% of your total body volume and may be given safely every 3 months Your body keeps on discarding and replenishing blood all the time whether you give blood or not, so this amount is quickly replenished.<br>
	</p>
<h7>AFTER DONATION</h7>
<p style="background-color: LightSalmon;">
	Relax & Refresh<br>
	You can have something to eat and drink in and relax for about 15-20 minutes belore departing Within 24-48 hours your blood volume is completely restored Apart from enjoying our retreshments and souvenir, other things to help you recover quickly include<br>
	• Drink plenty of water, fruit juice etc. and avoid alcohol for 8 hours after you've given blood.<br>
	• Avoid any strenuous activity or heavy lifting with the 'donating arm for 24 hours after you've given blood.<br>
	• If you become unwell in any way after giving blood, speak to the doctor in-charge straight away.<br>
	</p>
